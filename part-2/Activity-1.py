no1 = int(input("Enter Value one: "))
no2 = int(input("Enter Value two: "))
no3 = int(input("Enter Value three: "))

max_value = max(no1, no2, no3)  # Find the maximum value
min_value = min(no1, no2, no3)  # Find the minimum value

print("Max Value is:", max_value)  # Print maximum value
print("Min Value is:", min_value)  # Print minimum value
